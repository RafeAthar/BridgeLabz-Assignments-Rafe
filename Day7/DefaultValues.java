// package com.basicjava;

public class DefaultValues {
    static short a;
    static byte b;
    static int c;
    static float d;
    static double e;
    static char f;
    static String g;
    static boolean h;
    public static void main(String[] args) {
        System.out.println("default short: " + a);
        System.out.println("default byte: " + b);
        System.out.println("default int: " + c);
        System.out.println("default float: " + d);
        System.out.println("default double: " + e);
        System.out.println("default char: " + f);
        System.out.println("default String: " + g);
        System.out.println("default bool: " + h);
    }
}
